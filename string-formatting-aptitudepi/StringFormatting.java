public class StringFormatting{
    public static void main(String[] args) {
      double money = 21.7;
      int num1 = 19;
      int num2 = -37;
      System.out.printf("The total price is $%.2f\n", money);
      System.out.printf("%8d\n", num1);
      System.out.printf("%-12d\n", num2);
      System.out.println("My favorite book is \"Percy Jackson\"");
      System.out.println("Hello\nGoodbye");
    }
  }                  